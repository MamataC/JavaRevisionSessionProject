package controlStatement;

public class exampleifEleseif {

	public static void main(String[] args) {

		int marks = 30;

		if (marks < 30) {

			System.out.println("marks is not less than 30");
		}

		else if (marks > 30) {

			System.out.println("marks is not greater than 30");
		}

		else {

			System.out.println("marks is equal to 30");
		}
	}

}
